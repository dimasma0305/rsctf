async fn registration_lock_closes_bulk_creation_precheck_race() {
    let harness = Harness::new().await;
    let public_id = Uuid::new_v4();
    let mut public_registration = crate::utils::database::begin_sqlx_transaction(&harness.pool)
        .await
        .unwrap();
    sqlx::query("SELECT pg_advisory_xact_lock($1)")
        .bind(crate::controllers::account::REGISTRATION_LOCK_ID)
        .execute(&mut *public_registration)
        .await
        .unwrap();

    let contender = tokio::spawn({
        let pool = harness.pool.clone();
        async move {
            provision_explicit_user(
                &pool,
                explicit_write("RACER", "RACER@EXAMPLE.TEST", "bulk-hash"),
                None,
                None,
            )
            .await
        }
    });
    tokio::task::yield_now().await;
    insert_user(
        &mut *public_registration,
        public_id,
        "racer",
        "racer@example.test",
        Role::User,
        "public-hash",
        "public-stamp",
    )
    .await;
    public_registration.commit().await.unwrap();

    let result = contender.await.unwrap().unwrap();
    assert_eq!(result.id, public_id);
    assert!(!result.created);
    let count: i64 = sqlx::query_scalar(
        r#"SELECT COUNT(*)::bigint FROM "AspNetUsers" WHERE normalized_email = 'RACER@EXAMPLE.TEST'"#,
    )
    .fetch_one(&harness.pool)
    .await
    .unwrap();
    assert_eq!(count, 1);
    harness.cleanup().await;
}

#[tokio::test]
