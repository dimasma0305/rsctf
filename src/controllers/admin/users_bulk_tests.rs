struct Harness {
    admin: sqlx::PgPool,
    pool: sqlx::PgPool,
    cache: InMemoryCache,
    schema: String,
}

impl Harness {
    async fn new() -> Self {
        let database_url = std::env::var("RSCTF_TEST_DATABASE_URL")
            .expect("RSCTF_TEST_DATABASE_URL must point to disposable PostgreSQL");
        let admin = PgPoolOptions::new()
            .max_connections(1)
            .connect(&database_url)
            .await
            .unwrap();
        let schema = format!("rsctf_bulk_identity_{}", Uuid::new_v4().simple());
        sqlx::query(&format!(r#"CREATE SCHEMA "{schema}""#))
            .execute(&admin)
            .await
            .unwrap();
        let options = PgConnectOptions::from_str(&database_url)
            .unwrap()
            .options([("search_path", schema.as_str())]);
        let pool = PgPoolOptions::new()
            .max_connections(4)
            .connect_with(options)
            .await
            .unwrap();
        sqlx::raw_sql(
            r#"
            CREATE TABLE "AspNetUsers" (
              id UUID PRIMARY KEY,
              user_name TEXT,
              normalized_user_name TEXT UNIQUE,
              email TEXT,
              normalized_email TEXT,
              email_confirmed BOOLEAN NOT NULL DEFAULT FALSE,
              password_hash TEXT,
              security_stamp TEXT,
              concurrency_stamp TEXT,
              phone_number TEXT,
              phone_number_confirmed BOOLEAN NOT NULL DEFAULT FALSE,
              two_factor_enabled BOOLEAN NOT NULL DEFAULT FALSE,
              lockout_end TIMESTAMPTZ,
              lockout_enabled BOOLEAN NOT NULL DEFAULT FALSE,
              access_failed_count INTEGER NOT NULL DEFAULT 0,
              role SMALLINT NOT NULL,
              ip TEXT NOT NULL DEFAULT '0.0.0.0',
              browser_fingerprint TEXT,
              last_signed_in_utc TIMESTAMPTZ NOT NULL DEFAULT NOW(),
              last_visited_utc TIMESTAMPTZ NOT NULL DEFAULT NOW(),
              register_time_utc TIMESTAMPTZ NOT NULL DEFAULT NOW(),
              bio TEXT NOT NULL DEFAULT '',
              real_name TEXT NOT NULL DEFAULT '',
              std_number TEXT NOT NULL DEFAULT '',
              exercise_visible BOOLEAN NOT NULL DEFAULT TRUE,
              avatar_hash TEXT
            );
            CREATE TABLE "Teams" (
              id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              name TEXT NOT NULL,
              bio TEXT,
              avatar_hash TEXT,
              locked BOOLEAN NOT NULL DEFAULT FALSE,
              deletion_pending BOOLEAN NOT NULL DEFAULT FALSE,
              invite_token TEXT NOT NULL,
              captain_id UUID NOT NULL
            );
            CREATE TABLE "TeamMembers" (
              id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              team_id INTEGER NOT NULL,
              user_id UUID NOT NULL,
              UNIQUE (team_id, user_id)
            );
            "#,
        )
        .execute(&pool)
        .await
        .unwrap();
        Self {
            admin,
            pool,
            cache: InMemoryCache::new(),
            schema,
        }
    }

    async fn cleanup(self) {
        self.pool.close().await;
        sqlx::query(&format!(r#"DROP SCHEMA "{}" CASCADE"#, self.schema))
            .execute(&self.admin)
            .await
            .unwrap();
    }
}

async fn insert_user(
    executor: impl sqlx::Executor<'_, Database = sqlx::Postgres>,
    id: Uuid,
    user_name: &str,
    email: &str,
    role: Role,
    password_hash: &str,
    security_stamp: &str,
) {
    sqlx::query(
        r#"INSERT INTO "AspNetUsers"
             (id, user_name, normalized_user_name, email, normalized_email,
              password_hash, security_stamp, role)
           VALUES ($1, $2, $3, $4, $5, $6, $7, $8)"#,
    )
    .bind(id)
    .bind(user_name)
    .bind(user_name.to_uppercase())
    .bind(email)
    .bind(email.to_uppercase())
    .bind(password_hash)
    .bind(security_stamp)
    .bind(role as i16)
    .execute(executor)
    .await
    .unwrap();
}

fn explicit_write<'a>(
    user_name: &'a str,
    email: &'a str,
    password_hash: &'a str,
) -> ExplicitUserWrite<'a> {
    ExplicitUserWrite {
        user_name,
        normalized_user_name: user_name,
        email,
        normalized_email: email,
        password_hash,
        phone: None,
        create_real_name: "",
        create_std_number: "",
        update_real_name: None,
        update_std_number: None,
        update_phone: None,
        now: Utc::now(),
    }
}

fn import_write<'a>(email: &'a str, password_hash: &'a str) -> ImportUserWrite<'a> {
    ImportUserWrite {
        email,
        normalized_email: email,
        base_user_name: "imported",
        password_hash,
        email_confirmed: true,
        create_real_name: "Imported User",
        create_std_number: "",
        create_phone: None,
        update_real_name: Some("Imported User"),
        update_std_number: None,
        update_phone: None,
        now: Utc::now(),
    }
}

fn credential_write<'a>(harness: &'a Harness, password: &'a str) -> ImportCredentialWrite<'a> {
    ImportCredentialWrite {
        cache: &harness.cache,
        password,
    }
}

struct BlockingSetCache {
    inner: InMemoryCache,
    set_started: std::sync::Mutex<Option<tokio::sync::oneshot::Sender<()>>>,
    release_set: tokio::sync::Mutex<Option<tokio::sync::oneshot::Receiver<()>>>,
}

impl BlockingSetCache {
    fn new() -> (
        Self,
        tokio::sync::oneshot::Receiver<()>,
        tokio::sync::oneshot::Sender<()>,
    ) {
        let (set_started, wait_for_set) = tokio::sync::oneshot::channel();
        let (release_set, wait_for_release) = tokio::sync::oneshot::channel();
        (
            Self {
                inner: InMemoryCache::new(),
                set_started: std::sync::Mutex::new(Some(set_started)),
                release_set: tokio::sync::Mutex::new(Some(wait_for_release)),
            },
            wait_for_set,
            release_set,
        )
    }
}

#[async_trait::async_trait]
impl Cache for BlockingSetCache {
    async fn get(&self, key: &str) -> Option<bytes::Bytes> {
        self.inner.get(key).await
    }

    async fn get_and_remove(&self, key: &str) -> Option<bytes::Bytes> {
        self.inner.get_and_remove(key).await
    }

    async fn compare_and_remove(&self, key: &str, expected: &[u8]) -> bool {
        self.inner.compare_and_remove(key, expected).await
    }

    async fn set_if_absent(
        &self,
        key: &str,
        value: &[u8],
        ttl: Option<std::time::Duration>,
    ) -> bool {
        self.inner.set_if_absent(key, value, ttl).await
    }

    async fn set(&self, key: &str, value: &[u8], ttl: Option<std::time::Duration>) {
        if let Some(set_started) = self.set_started.lock().unwrap().take() {
            let _ = set_started.send(());
        }
        let release_set = { self.release_set.lock().await.take() };
        if let Some(release_set) = release_set {
            release_set.await.unwrap();
        }
        self.inner.set(key, value, ttl).await;
    }

    async fn remove(&self, key: &str) {
        self.inner.remove(key).await;
    }
}

#[tokio::test]
#[ignore = "requires PostgreSQL via RSCTF_TEST_DATABASE_URL"]
async fn explicit_bulk_identity_rejects_cross_owner_matches() {
    let harness = Harness::new().await;
    let alice = Uuid::new_v4();
    let bob = Uuid::new_v4();
    insert_user(
        &harness.pool,
        alice,
        "alice",
        "alice@example.test",
        Role::User,
        "alice-old",
        "alice-stamp",
    )
    .await;
    insert_user(
        &harness.pool,
        bob,
        "bob",
        "bob@example.test",
        Role::User,
        "bob-old",
        "bob-stamp",
    )
    .await;

    let error = provision_explicit_user(
        &harness.pool,
        explicit_write("ALICE", "BOB@EXAMPLE.TEST", "new-hash"),
        None,
        None,
    )
    .await
    .expect_err("username owner was re-credentialed with another account's email");
    assert_eq!(error.status(), axum::http::StatusCode::CONFLICT);
    let rows: Vec<(Uuid, Option<String>, Option<String>)> = sqlx::query_as(
        r#"SELECT id, password_hash, security_stamp FROM "AspNetUsers" ORDER BY id"#,
    )
    .fetch_all(&harness.pool)
    .await
    .unwrap();
    assert!(rows.contains(&(alice, Some("alice-old".into()), Some("alice-stamp".into()))));
    assert!(rows.contains(&(bob, Some("bob-old".into()), Some("bob-stamp".into()))));
    harness.cleanup().await;
}

