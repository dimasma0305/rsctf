async fn failed_team_assignment_rolls_back_recredential_and_creation() {
    let harness = Harness::new().await;
    let captain = Uuid::new_v4();
    let existing = Uuid::new_v4();
    for (id, name, email) in [
        (captain, "captain", "captain@example.test"),
        (existing, "existing", "existing@example.test"),
    ] {
        insert_user(
            &harness.pool,
            id,
            name,
            email,
            Role::User,
            "old-hash",
            "old-stamp",
        )
        .await;
    }
    sqlx::query(
        r#"INSERT INTO "Teams"
             (id, name, deletion_pending, invite_token, captain_id)
           VALUES (1, 'deleting', TRUE, 'token', $1),
                  (2, 'full', FALSE, 'token', $1)"#,
    )
    .bind(captain)
    .execute(&harness.pool)
    .await
    .unwrap();
    let synthetic_members: Vec<Uuid> = (0..99).map(|_| Uuid::new_v4()).collect();
    sqlx::query(
        r#"INSERT INTO "TeamMembers" (team_id, user_id)
           SELECT 2, member_id FROM UNNEST($1::uuid[]) AS member_id"#,
    )
    .bind(&synthetic_members)
    .execute(&harness.pool)
    .await
    .unwrap();

    let deleting = provision_import_user(
        &harness.pool,
        import_write("EXISTING@EXAMPLE.TEST", "new-delete-hash"),
        credential_write(&harness, "new-delete-password"),
        Some("deleting"),
        None,
    )
    .await
    .expect_err("deletion-pending team accepted an import");
    assert_eq!(deleting.status(), axum::http::StatusCode::CONFLICT);
    let full = provision_import_user(
        &harness.pool,
        import_write("EXISTING@EXAMPLE.TEST", "new-full-hash"),
        credential_write(&harness, "new-full-password"),
        Some("full"),
        None,
    )
    .await
    .expect_err("full team accepted an import");
    assert_eq!(full.status(), axum::http::StatusCode::BAD_REQUEST);
    let identity: (Option<String>, Option<String>) =
        sqlx::query_as(r#"SELECT password_hash, security_stamp FROM "AspNetUsers" WHERE id = $1"#)
            .bind(existing)
            .fetch_one(&harness.pool)
            .await
            .unwrap();
    assert_eq!(
        identity,
        (Some("old-hash".into()), Some("old-stamp".into()))
    );

    let error = provision_import_user(
        &harness.pool,
        import_write("NEW@EXAMPLE.TEST", "undisclosed-hash"),
        credential_write(&harness, "undisclosed-password"),
        Some("full"),
        None,
    )
    .await
    .expect_err("full team left a newly-created account behind");
    assert_eq!(error.status(), axum::http::StatusCode::BAD_REQUEST);
    let orphan_count: i64 = sqlx::query_scalar(
        r#"SELECT COUNT(*)::bigint FROM "AspNetUsers" WHERE normalized_email = 'NEW@EXAMPLE.TEST'"#,
    )
    .fetch_one(&harness.pool)
    .await
    .unwrap();
    assert_eq!(orphan_count, 0);
    assert!(
        harness
            .cache
            .get(
                &crate::controllers::admin::users_credentials::credential_cache_key(
                    "existing@example.test",
                )
            )
            .await
            .is_none(),
        "a rolled-back recredential published plaintext"
    );
    assert!(
        harness
            .cache
            .get(
                &crate::controllers::admin::users_credentials::credential_cache_key(
                    "new@example.test",
                )
            )
            .await
            .is_none(),
        "a rolled-back account creation published plaintext"
    );
    harness.cleanup().await;
}

