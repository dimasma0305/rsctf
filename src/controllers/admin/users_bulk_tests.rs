async fn later_full_team_failure_is_a_skipped_row_without_losing_other_results() {
    use super::super::users::{import_row_step, ImportUserResult};
    use crate::controllers::admin::users_credentials::credential_cache_key;

    let harness = Harness::new().await;
    let captain = Uuid::new_v4();
    insert_user(
        &harness.pool,
        captain,
        "captain",
        "captain@example.test",
        Role::User,
        "captain-hash",
        "captain-stamp",
    )
    .await;
    sqlx::query(
        r#"INSERT INTO "Teams"
             (id, name, deletion_pending, invite_token, captain_id)
           VALUES (1, 'full', FALSE, 'token', $1)"#,
    )
    .bind(captain)
    .execute(&harness.pool)
    .await
    .unwrap();
    let synthetic_members: Vec<Uuid> = (0..99).map(|_| Uuid::new_v4()).collect();
    sqlx::query(
        r#"INSERT INTO "TeamMembers" (team_id, user_id)
           SELECT 1, member_id FROM UNNEST($1::uuid[]) AS member_id"#,
    )
    .bind(&synthetic_members)
    .execute(&harness.pool)
    .await
    .unwrap();

    let first = import_row_step(
        provision_import_user(
            &harness.pool,
            import_write("FIRST@EXAMPLE.TEST", "first-hash"),
            credential_write(&harness, "first-password"),
            None,
            None,
        )
        .await,
        1,
        "provision",
    )
    .unwrap();
    let ImportProvision::Provisioned(first) = first else {
        panic!("first row was unexpectedly skipped");
    };
    let mut response_rows = vec![ImportUserResult {
        email: "first@example.test".to_string(),
        real_name: "First".to_string(),
        user_name: first.user_name,
        password: "first-password".to_string(),
        team_name: None,
        status: "created".to_string(),
        error: None,
    }];

    let second_reason = import_row_step(
        provision_import_user(
            &harness.pool,
            import_write("SECOND@EXAMPLE.TEST", "second-hash"),
            credential_write(&harness, "second-password"),
            Some("full"),
            Some(1),
        )
        .await,
        2,
        "provision",
    )
    .expect_err("full team unexpectedly accepted the second row");
    response_rows.push(ImportUserResult {
        email: "second@example.test".to_string(),
        real_name: "Second".to_string(),
        user_name: "imported.2".to_string(),
        password: String::new(),
        team_name: Some("full".to_string()),
        status: "skipped".to_string(),
        error: Some(second_reason),
    });

    let third = import_row_step(
        provision_import_user(
            &harness.pool,
            import_write("THIRD@EXAMPLE.TEST", "third-hash"),
            credential_write(&harness, "third-password"),
            None,
            None,
        )
        .await,
        3,
        "provision",
    )
    .unwrap();
    let ImportProvision::Provisioned(third) = third else {
        panic!("third row did not continue after the second row was skipped");
    };
    response_rows.push(ImportUserResult {
        email: "third@example.test".to_string(),
        real_name: "Third".to_string(),
        user_name: third.user_name,
        password: "third-password".to_string(),
        team_name: None,
        status: "created".to_string(),
        error: None,
    });

    assert_eq!(response_rows.len(), 3);
    assert_eq!(response_rows[0].password, "first-password");
    assert_eq!(response_rows[1].status, "skipped");
    assert_eq!(response_rows[1].password, "");
    assert_eq!(response_rows[1].error.as_deref(), Some("Team is full"));
    assert_eq!(response_rows[2].password, "third-password");
    assert!(
        harness
            .cache
            .get(&credential_cache_key("first@example.test"))
            .await
            .is_some(),
        "later failure removed the first committed credential"
    );
    assert!(
        harness
            .cache
            .get(&credential_cache_key("second@example.test"))
            .await
            .is_none(),
        "rolled-back second row published plaintext"
    );

    harness.cleanup().await;
}

#[tokio::test]
#[ignore = "requires PostgreSQL via RSCTF_TEST_DATABASE_URL"]
async fn credential_publication_precedes_a_concurrent_email_mutation() {
    use crate::controllers::admin::users_credentials::{
        credential_cache_key, CachedImportCredential,
    };

    let harness = Harness::new().await;
    let user_id = Uuid::new_v4();
    insert_user(
        &harness.pool,
        user_id,
        "alice",
        "old@example.test",
        Role::User,
        "old-hash",
        "old-stamp",
    )
    .await;

    let (cache, publication_started, release_publication) = BlockingSetCache::new();
    let cache = std::sync::Arc::new(cache);
    let import_pool = harness.pool.clone();
    let import_cache = cache.clone();
    let import = tokio::spawn(async move {
        provision_import_user(
            &import_pool,
            import_write("OLD@EXAMPLE.TEST", "new-hash"),
            ImportCredentialWrite {
                cache: import_cache.as_ref(),
                password: "new-password",
            },
            None,
            None,
        )
        .await
    });
    publication_started.await.unwrap();

    let mutation_pool = harness.pool.clone();
    let (mutation_started, wait_for_mutation) = tokio::sync::oneshot::channel();
    let mut mutation = tokio::spawn(async move {
        mutation_started.send(()).unwrap();
        let mut transaction = registration_transaction(&mutation_pool).await.unwrap();
        sqlx::query(
            r#"UPDATE "AspNetUsers"
                  SET email = 'new@example.test',
                      normalized_email = 'NEW@EXAMPLE.TEST'
                WHERE id = $1"#,
        )
        .bind(user_id)
        .execute(&mut *transaction)
        .await
        .unwrap();
        transaction.commit().await.unwrap();
    });
    wait_for_mutation.await.unwrap();
    assert!(
        tokio::time::timeout(std::time::Duration::from_millis(100), &mut mutation)
            .await
            .is_err(),
        "email mutation crossed the import's publication/commit boundary"
    );

    release_publication.send(()).unwrap();
    let provision = import.await.unwrap().unwrap();
    let ImportProvision::Provisioned(provision) = provision else {
        panic!("the import was hidden after committing its generated password");
    };
    assert_eq!(provision.id, user_id);
    assert!(!provision.created);
    tokio::time::timeout(std::time::Duration::from_secs(2), mutation)
        .await
        .expect("email mutation stayed blocked after the import committed")
        .unwrap();

    let identity: (Option<String>, Option<String>, Option<String>) = sqlx::query_as(
        r#"SELECT normalized_email, password_hash, security_stamp
             FROM "AspNetUsers" WHERE id = $1"#,
    )
    .bind(user_id)
    .fetch_one(&harness.pool)
    .await
    .unwrap();
    assert_eq!(identity.0.as_deref(), Some("NEW@EXAMPLE.TEST"));
    assert_eq!(identity.1.as_deref(), Some("new-hash"));
    assert_eq!(
        identity.2.as_deref(),
        Some(provision.security_stamp.as_str())
    );

    // This is the worst case if a caller mutates the email but crashes before
    // invalidating the old key: delivery still rejects it because its embedded
    // id/email/stamp tuple no longer matches the live row.
    let value = cache
        .get(&credential_cache_key("old@example.test"))
        .await
        .expect("credential was not published before commit");
    let credential: CachedImportCredential = serde_json::from_slice(&value).unwrap();
    assert_eq!(credential.user_id, user_id);
    assert_eq!(credential.security_stamp, provision.security_stamp);
    assert_eq!(credential.password, "new-password");

    harness.cleanup().await;
}

#[tokio::test]
#[ignore = "requires PostgreSQL via RSCTF_TEST_DATABASE_URL"]
async fn failed_commit_removes_the_just_published_plaintext() {
    use crate::controllers::admin::users_credentials::credential_cache_key;

    let harness = Harness::new().await;
    sqlx::raw_sql(
        r#"
        CREATE FUNCTION reject_import_commit() RETURNS trigger LANGUAGE plpgsql AS $$
        BEGIN
          IF NEW.password_hash = 'commit-fail-hash' THEN
            RAISE EXCEPTION 'forced deferred import failure';
          END IF;
          RETURN NEW;
        END;
        $$;
        CREATE CONSTRAINT TRIGGER reject_import_commit
          AFTER INSERT OR UPDATE ON "AspNetUsers"
          DEFERRABLE INITIALLY DEFERRED
          FOR EACH ROW EXECUTE FUNCTION reject_import_commit();
        "#,
    )
    .execute(&harness.pool)
    .await
    .unwrap();

    let error = provision_import_user(
        &harness.pool,
        import_write("ROLLBACK@EXAMPLE.TEST", "commit-fail-hash"),
        credential_write(&harness, "must-not-survive"),
        None,
        None,
    )
    .await
    .expect_err("deferred constraint unexpectedly allowed the import to commit");
    assert_eq!(
        error.status(),
        axum::http::StatusCode::INTERNAL_SERVER_ERROR
    );
    let user_count: i64 = sqlx::query_scalar(
        r#"SELECT COUNT(*)::bigint FROM "AspNetUsers"
            WHERE normalized_email = 'ROLLBACK@EXAMPLE.TEST'"#,
    )
    .fetch_one(&harness.pool)
    .await
    .unwrap();
    assert_eq!(user_count, 0, "failed commit left the account live");
    assert!(
        harness
            .cache
            .get(&credential_cache_key("rollback@example.test"))
            .await
            .is_none(),
        "failed commit left its plaintext cache publication live"
    );

    harness.cleanup().await;
}

